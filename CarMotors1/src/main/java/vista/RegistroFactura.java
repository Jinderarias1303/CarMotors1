      private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
    String clienteStr = id_cliente.getText().trim();
    String mantenimientoStr = id_mantenimiento.getText().trim();
    String fechaStr = fecha_emision.getText().trim();
    String subtotalStr = sub_total.getText().trim();
    String impuestoStr = Impuestos.getText().trim();
    String totalStr = Total.getText().trim();
    String cufeStr = cufe.getText().trim();
    String qrCodeStr = qr_code.getText().trim();

    // Validaci√≥n de campos vac√≠os
    if (clienteStr.isEmpty() || mantenimientoStr.isEmpty() || fechaStr.isEmpty()
            || subtotalStr.isEmpty() || impuestoStr.isEmpty()
            || totalStr.isEmpty() || cufeStr.isEmpty() || qrCodeStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Completa todos los campos.");
        return;
    }

    try {
        int idCliente = Integer.parseInt(clienteStr);
        int idMantenimiento = Integer.parseInt(mantenimientoStr);
        LocalDate fecha = LocalDate.parse(fechaStr);  // Asegura formato yyyy-MM-dd
        BigDecimal subtotal = new BigDecimal(subtotalStr);
        BigDecimal impuesto = new BigDecimal(impuestoStr);
        BigDecimal total = new BigDecimal(totalStr);


            boolean exito = facturacontroller.RegistrarFactura(
                           idCliente,
                           idMantenimiento,
                           fecha,
                           subtotal,
                           impuesto,
                           total,
                           cufeStr,
                           qrCodeStr
        );

        if (exito) {
            JOptionPane.showMessageDialog(this, "‚úÖ Factura registrada exitosamente.");
            id_cliente.setText("");
            id_mantenimiento.setText("");
            fecha_emision.setText("");
            sub_total.setText("");
            Impuestos.setText("");
            Total.setText("");
            cufe.setText("");
            qr_code.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "‚ùå Error al registrar la factura.");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Verifica que los campos num√©ricos (cliente, mantenimiento, valores) sean v√°lidos.");
        e.printStackTrace();
    } catch (DateTimeParseException e) {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è La fecha debe tener el formato yyyy-MM-dd.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "‚ùå Error inesperado al registrar la factura.");
        e.printStackTrace();
    }

      }//GEN-LAST:event_RegistrarActionPerformed

